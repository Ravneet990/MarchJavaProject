package addTwoNumbers;

public class TwoNumbers {

	int num1 = 5;

	int num2 = 6;

	int num3;

	void addTwoNumbers() {

		num3 = num1 + num2;

		System.out.println(num3);
	}

	void multiplyTwoNumbers() {

		num3 = num1 * num2;

		System.out.println(num3);

	}

	void divideTwoNumbers() {

		num3 = num1 / num2;

		System.out.println(num3);
	}

	void getRemainder() {

		num3 = num1 % num2;

		System.out.println(num3);
	}

}
